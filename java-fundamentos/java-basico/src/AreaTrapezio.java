public class AreaTrapezio {
    public static void main(String[] args) {
        
        double baseMenor, baseMaior, area;

        baseMenor = 6.0;
        baseMaior = 8.0;
        area = 5.0;

        area = (baseMenor + baseMaior) / 2.0 * area;

        System.out.println(area);

    }
    
}
